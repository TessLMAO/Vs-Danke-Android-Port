
function onBeatHit( ... )
	if curBeat == 96 then

        setProperty('i.visible',true)
	end
	if curBeat == 160 then

        setProperty('i.visible',false)
	end

	if curBeat == 319 then

        setProperty('i.visible',true)
	end
	if curBeat == 384 then

        setProperty('i.visible',false)
	end
end