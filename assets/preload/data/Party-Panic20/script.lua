
function onBeatHit( ... )
	if curBeat == 160 then

        setProperty('i.visible',true)
	end
	if curBeat == 224 then

        setProperty('i.visible',false)
	end
	if curBeat == 416 then

        setProperty('i.visible',true)
	end
	if curBeat == 480 then

        setProperty('i.visible',false)
	end

end